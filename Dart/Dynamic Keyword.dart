void main() {
  //! Dynamic Keyword in Dart
  //? dynamic Keyword is variable in Dart without specifying it's data types but when we need to change data type then chnage it

  dynamic a = 5;
  print(a);

  a = 15;
  print(a);
  a = 2.5;
  print(a);
  a = true;
  print(a);
  a = "Hello";
  print(a);
}
