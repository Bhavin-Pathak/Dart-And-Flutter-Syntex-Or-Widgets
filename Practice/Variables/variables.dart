// VAR Keyword : Variable is used to store values

var name = "Bhavin";
// var Keyword -> variable name -> = asignoperator -> values

const sname = "";
// const Keyword -> constant name -> = asignoperator -> values
// we cannot assign new value to constants

/* HERE IS EXAMPLE OF TYPE ANOTATION OF VARIABLES */

int number = 20; // To Store Numeric Values
// int datatype -> variable name -> = asignoperator -> values

String greeting = "Hello World!"; // To Store String Values
// String datatype -> variable name -> = asignoperator -> values

bool isTrue = true; // To Store Boolean Values (TRUE,FALSE)
// boolean datatype -> variable name -> = asignoperator -> values

double values = 2.2; // To Store Floating Point Numeric Values
// double datatype -> variable name -> = asignoperator -> values
